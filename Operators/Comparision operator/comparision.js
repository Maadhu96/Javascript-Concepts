//Comparision operator
//==(Equal to)

let a = 20;
let b = 30;

console.log(a == b)

let c = 30;
let d = 30;
console.log(c == d)

//===(Strictly equal to)
let e = 30;
let f = 40;
console.log(e === f)

let g = 50;
let h = 50;
console.log(g === h)

//(!==)Not equal to
let i = 30;
let j = 25;
console.log(i != j)

let k = 30;
let l = 30;
console.log(k != l)

//(!==) strictly Not-equal
let m = 50;
let n = 40;
console.log(m!==n)

let o = 40;
let p = 40;
console.log(o !== p)

//(<,>,<=,>= are used to compare the operands wheather it is greater than or less than)